
public class BussinessLogic 
{
   
    
    public int ITCP(int ITCPSessional, int ITCPMid, int ITCPFinal)
    {
        return ITCPSessional+ITCPMid+ITCPFinal;
    }
    
    public int CP(int CPSessional, int CPMid, int CPFinal)
    {
        return CPSessional+CPMid+CPFinal;
    }
    
    public int OOP(int OOPSessional, int OOPMid, int OOPFinal)
    {
        return OOPSessional+OOPMid+OOPFinal;
    }
    
    public int VP(int VPSessional, int VPMid, int VPFinal)
    {
        return VPSessional+VPMid+VPFinal;
    }
    
    public int FP(int FPSessional, int FPMid, int FPinal)
    {
        return FPSessional+FPMid+FPinal;
    }
    
    public int TotalResult(int ResultITCPL, int ResultCPL, int ResultOOPL, int ResultVPL, int ResultIFPL)
    {
        return ResultITCPL+ResultCPL+ResultOOPL+ResultVPL+ResultIFPL;
        
    }
    
    
    

    
}
